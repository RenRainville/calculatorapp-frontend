// CreateGraph function, takes startPts and endPts, creates an edge and a sphere on the endPt as Node
function createGraph(scene, startPoints, endPoints, color, thickness) {
    const cylinderMaterial = new THREE.MeshBasicMaterial({ color: color });
    const sphereMaterial = new THREE.MeshBasicMaterial({ color: color });

    for (let i = 0; i < startPoints.length; i++) {
        const start = new THREE.Vector3(startPoints[i][0], startPoints[i][1], startPoints[i][2]);
        const end = new THREE.Vector3(endPoints[i][0], endPoints[i][1], endPoints[i][2]);
        const direction = end.clone().sub(start);
        const length = direction.length();

        const cylinderGeometry = new THREE.CylinderGeometry(thickness, thickness, length, 8);
        const cylinder = new THREE.Mesh(cylinderGeometry, cylinderMaterial);

        // Position the cylinder at the midpoint between start and end
        cylinder.position.copy(start.clone().add(end).multiplyScalar(0.5));

        // Set the rotation of the cylinder using a quaternion
        const quaternion = new THREE.Quaternion();
        quaternion.setFromUnitVectors(new THREE.Vector3(0, 1, 0), direction.clone().normalize());
        cylinder.setRotationFromQuaternion(quaternion);

        scene.add(cylinder);
        
        // Add a sphere at the end point
        const sphereGeometry = new THREE.SphereGeometry(thickness * 3, 16, 16);
        const sphere = new THREE.Mesh(sphereGeometry, sphereMaterial);
        sphere.position.copy(end);
        scene.add(sphere);
    }
}

